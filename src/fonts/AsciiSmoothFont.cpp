bool lookupAsciiSmoothGlyph(char c, const uint8_t *&data, uint8_t &width,
                            uint8_t &height, uint8_t &row_bytes,
                            uint8_t &bits_per_pixel) {
  data = nullptr;
  width = 0;
  height = 0;
  row_bytes = 0;
  bits_per_pixel = 0;
  int index = glyphIndexForChar(c);
  if (index < 0) index = glyphIndexForChar('?');
  if (index < 0) return false;
  width = kGlyphWidths[index];
  height = kAsciiSmoothFontPx;
  row_bytes = kGlyphRowBytes[index];
  bits_per_pixel = kAsciiSmoothFontBitsPerPixel;
  data = &kGlyphData[kGlyphOffsets[index]];
  return true;
}

