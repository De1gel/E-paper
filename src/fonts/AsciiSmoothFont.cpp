int glyphIndexForChar(char c) {
  if (c >= 'a' && c <= 'z') c = static_cast<char>(c - 'a' + 'A');
  for (size_t i = 0; i < kAsciiSmoothGlyphCount; ++i) {
    if (kGlyphChars[i] == c) return static_cast<int>(i);
  }
  return -1;
}

